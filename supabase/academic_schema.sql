-- Academic modules schema for NCF COE Blockchain Grade System
-- Run in Supabase SQL editor (safe to re-run).

create table if not exists public.faculty_records (
  id text primary key,
  name text not null,
  dept text not null,
  rank text,
  status text,
  created_at timestamp with time zone default now()
);

create table if not exists public.teaching_loads (
  id bigint generated by default as identity primary key,
  faculty_id text not null references public.faculty_records(id) on delete cascade,
  faculty text not null,
  subject text not null,
  section text not null,
  units integer,
  schedule text,
  created_at timestamp with time zone default now()
);

create table if not exists public.curriculum_subjects (
  code text primary key,
  title text not null,
  units integer,
  program text,
  year text,
  semester text,
  created_at timestamp with time zone default now()
);

create table if not exists public.sections (
  id text primary key,
  name text not null,
  program text,
  year text,
  semester text,
  created_at timestamp with time zone default now()
);

create table if not exists public.section_subjects (
  section_id text not null references public.sections(id) on delete cascade,
  subject_code text not null,
  created_at timestamp with time zone default now(),
  primary key (section_id, subject_code)
);

create table if not exists public.section_students (
  section_id text not null references public.sections(id) on delete cascade,
  student_id text not null,
  created_at timestamp with time zone default now(),
  primary key (section_id, student_id)
);

