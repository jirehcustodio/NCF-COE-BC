create table if not exists public.enrollment_records (
  id text primary key,
  name text not null,
  program text,
  section text,
  status text,
  adviser text,
  created_at timestamp with time zone default now()
);

create table if not exists public.grade_sheets (
  id bigint generated by default as identity primary key,
  subject text not null,
  section text not null,
  period text not null,
  last_updated text,
  status text,
  created_at timestamp with time zone default now()
);

create table if not exists public.students (
  id text not null,
  name text not null,
  subj text not null,
  prof text not null,
  prelim integer,
  midterm integer,
  semi integer,
  final integer,
  status text,
  upload_method text,
  encoded_at timestamp with time zone,
  created_at timestamp with time zone default now(),
  primary key (id, subj)
);

create table if not exists public.subjects (
  id bigint generated by default as identity primary key,
  code text not null,
  prof text not null,
  title text,
  created_at timestamp with time zone default now(),
  unique (code, prof)
);

create table if not exists public.user_profiles (
  id text primary key,
  name text not null,
  role text,
  avatar_url text,
  created_at timestamp with time zone default now()
);

