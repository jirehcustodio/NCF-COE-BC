drop policy if exists "faculty_records_read" on public.faculty_records;
create policy "faculty_records_read" on public.faculty_records
  for select using (true);

drop policy if exists "faculty_records_insert" on public.faculty_records;
create policy "faculty_records_insert" on public.faculty_records
  for insert with check (auth.jwt()->>'email' = id);

drop policy if exists "faculty_records_update" on public.faculty_records;
create policy "faculty_records_update" on public.faculty_records
  for update using (auth.jwt()->>'email' = id);

drop policy if exists "faculty_records_delete" on public.faculty_records;
create policy "faculty_records_delete" on public.faculty_records
  for delete using (
    auth.jwt()->>'email' = id
    OR auth.jwt()->'user_metadata'->>'role' = 'dean'
